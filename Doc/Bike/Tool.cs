梅花扳手或开口扳手、是维修自行车常用扳手。

修自行车，尽量不用活络扳手。最好用套管扳手 。其次是梅花扳手。这样对螺母损害小。
推荐 8-10 12-14 13-15 14-17 双头梅花扳手一套。双头开口扳手一套。内六角扳手一套（拧车把用）。基本就够用了。
备用一个10寸活络扳手，一把管钳。钳子 螺丝刀数把。

1.活动扳手或开口扳手：一般五金店便可购得，开口扳手使用上较方便，但是购买时须注意一般自行车所用的多为公制螺栓；而有一支活动扳手，在许多工作上是必须的。
2.内六角扳手：一般五金行均可购得，注意一般使用的均为公制。
3.螺丝起子：有一字与十字两种，五金行即可购得。
4.拆胎杆：拆卸外胎时使用，可在车店购得。
5.补胎胶与补胎片：补胎用，可在车店购得。
6.打链器：拆卸链条使用。
7.钢丝扳手：调整轮圈钢丝张力用。
8.润滑油：虽不是工具的一种，但是是维护自行车时所必备的，要准备黄油、链条油、及一般滑润油三种。
9.特殊工具（非必要，但有最好）
10.套筒扳手：使用于拆卸曲柄的固定螺栓，一般为14mm或15mm的。
11.曲柄拆卸器：用于拆卸曲柄或大齿盘。
12.链条扳手：将飞轮的链轮分解使用。
13.飞轮拆卸器：将飞轮拆下使用，不同厂牌的飞轮须用不同的拆卸器。
14.花鼓扳手：为薄片状的开口扳手，用来分解花鼓轴及轴承，一般为13mm至16mm。
15.前叉碗扳手：薄片状的开口扳手，用于装卸调整前叉碗，依前叉碗的尺寸不同而有不同。
16.橡胶锤：用来敲击卡住的零件，使其松脱。
17.剪线钳：钳口呈圆弧形，剪内线时才不会散开。
18.BB工具组：用来拆卸调整中天心，依不同形式的中天心而有不同形式。
19.工作台：用于将自行车架起，以便进行维修工作。
20.调轮架：调整轮圈钢丝张力使用。 
